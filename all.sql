SELECT E.FNAME,
E.LNAME,
E.SALARY * 1.1 AS INCREASED_SALARY
FROM EMPLOYEE E
JOIN DEPARTMENT D
ON E.DNO = D.DNUMBER
WHERE D.DNAME='Research';

SELECT
SUM(E.SALARY) AS TOTAL,
MAX(E.SALARY) AS MAX,
MIN(E.SALARY) AS MIN,
AVG(E.SALARY) AS AVERAGE
FROM EMPLOYEE E
JOIN DEPARTMENT D
ON E.DNO = D.DNUMBER
WHERE D.DNAME='Finance';

SELECT FNAME, LNAME
FROM EMPLOYEE
WHERE DNO = 5;

SELECT D.DNAME,
COUNT(*) AS EMP_COUNT
FROM EMPLOYEE E
JOIN DEPARTMENT D
ON E.DNO = D.DNUMBER
GROUP BY D.DNUMBER, D.DNAME
HAVING COUNT(*) >= 2;

SELECT FNAME,LNAME,BDATE
FROM EMPLOYEE
