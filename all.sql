CREATE TABLE DEPARTMENT(
DNAME VARCHAR2(20),
DNUMBER NUMBER PRIMARY KEY,
MGRSSN VARCHAR2(20),
MGRSTARTDATE DATE
);



CREATE TABLE EMPLOYEE(
FNAME VARCHAR2(20),
MINIT CHAR(1),
LNAME VARCHAR2(20),
SSN VARCHAR2(15) PRIMARY KEY,
BDATE DATE,
ADDRESS VARCHAR2(100),
SEX CHAR(1),
SALARY NUMBER,
SUPERSSN VARCHAR2(15),
DNO NUMBER,
CONSTRAINT FK_DEPT
FOREIGN KEY (DNO)
REFERENCES DEPARTMENT(DNUMBER)
);

